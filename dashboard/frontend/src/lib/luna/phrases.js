/**
 * Seleciona uma frase aleatória de um pool, evitando repetir as últimas N
 * @param {string[]} pool - Array de frases
 * @param {string[]} history - Array de frases recentemente usadas
 * @param {number} avoidLast - Quantas últimas evitar (default: 3)
 * @returns {string} Frase selecionada
 */
export function pickPhrase(pool, history, avoidLast = 3) {
  const recent = history.slice(-avoidLast);
  const available = pool.filter((p) => !recent.includes(p));
  const candidates = available.length > 0 ? available : pool;
  return candidates[Math.floor(Math.random() * candidates.length)];
}

/**
 * Obtém pool de frases por estado/tool
 * @param {string} state - Estado da Luna
 * @param {string|null} tool - Tool em uso (para working)
 * @returns {string[]} Pool de frases
 */
export function getPhrasePool(state, tool = null) {
  switch (state) {
    case "sleep":
      return LUNA_PHRASES.sleepToWake;
    case "idle":
      return LUNA_PHRASES.idle;
    case "thinking":
      return LUNA_PHRASES.thinking;
    case "working":
      if (tool && LUNA_PHRASES.working[tool]) {
        return LUNA_PHRASES.working[tool];
      }
      return LUNA_PHRASES.working.default;
    case "success":
      return LUNA_PHRASES.success;
    case "error":
      return LUNA_PHRASES.error;
    case "walk":
      return LUNA_PHRASES.walk;
    case "run":
      return LUNA_PHRASES.runToClick;
    case "timeout":
      return LUNA_PHRASES.timeout;
    default:
      return LUNA_PHRASES.idle;
  }
}
